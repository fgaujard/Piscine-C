int		ft_iterative_factorial(int nb)
{
	int i;

	i = nb;
	if ((nb >= 0) && (nb < 13))
	{
		if (nb == 1 || nb == 0)
			return (1);
		while (nb > 1)
		{
			i = i * (nb - 1);
			nb--;
		}
		return (i);
	}
	else
		return (0);
}
