int		ft_find_next_prime(int nb)
{
	int i;

	i = 2;
	if (nb <= 1)
		nb = 2;
	while (i <= nb)
	{
		if (nb % i == 0)
		{
			if (nb == i)
				return (nb);
			else
			{
				i = 1;
				nb++;
			}
		}
		i++;
	}
	return (0);
}
